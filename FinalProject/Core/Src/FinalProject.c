/* USER CODE BEGIN 4 */

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {

	if (htim == &htim7) {

		command = command2;

		count = TIM1->CNT;	//get encoder counts
		if (count > 32768) {

			count = count - 65536;
		}

		angle = (float)(count) * (360.0 / (float)enc);

		temp2 = angle;
		rate = ((temp2 - temp1) / Ts) * (60.0 / 360.0);	//in rpm
		temp1 = temp2;

		if (rate < 0) {

			rate = rate_old;
		}
		else {

			rate_old = rate;
		}

		error = (command - rate) / Kg;

		p = p_old + Kp*( error - error_old) + Ts*Ki*error_old;	//PI Controller
		p_old = p;
		error_old = error;

		pulse = (uint32_t) ((float) 9999*p);	//closed loop imp.
		//pulse = (uint32_t) ((float) 9999*command/Kg); 	//open loop imp.
		if (pulse > 9999) {

			pulse = 9999;
		}
		else if (pulse < 0) {

			pulse = 0;
		}

		//Simulation of closed-loop system
		sim_out = sim_out_old + Ts*(-(1/Tm)*sim_out_old + (1.3*Kg/Tm)*p);
		sim_out_old = sim_out;

		//Simulation of open-loop system
		open_loop = open_loop_old + Ts*(-(1/Tm)*open_loop_old + (1/Tm)*command);
		open_loop_old = open_loop;

		//pulse = 4999;
		__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, pulse);

		printf("$%0.2f %0.2f %0.2f %0.2f %0.2f %0.2f %ld;\r\n", command, rate, sim_out, open_loop, p, error, pulse);
	}
}

